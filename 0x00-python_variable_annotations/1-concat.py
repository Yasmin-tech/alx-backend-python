''' This model contains the type-annotated function concat:
    takes a string str1 and a string str2 as arguments
    and return a concatenated string
    '''


def concat(str1: str, str2: str) -> str:
    ''' return a concatenated string str1 + str2
        str1 and str2 they should be a string type
        '''
    return str1 + str2
